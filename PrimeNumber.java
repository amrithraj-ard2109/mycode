public class PrimeNumber {

	public static void main(String[] args) {
		int prime=17;
		if(prime%2!=0)
		{
			System.out.println(prime +" " + "is a prime number");
		}
		else 
		{
			System.out.println(prime +" " + "is not a prime number");
		}

	}

}
